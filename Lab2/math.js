//add
addnumbers=(a,b)=>{
    return a+b;
}
    
//sub
subnumbers=(a,b)=>{
    return a-b;
}

//mul
mulnumbers=(a,b)=>{
    return a*b;
}

//div
divnumbers=(a,b)=>{
    return a/b;
}

//mod
modnumbers=(a,b)=>{
    return a%b;
}
module.exports=({addnumbers,subnumbers,mulnumbers,divnumbers,modnumbers});